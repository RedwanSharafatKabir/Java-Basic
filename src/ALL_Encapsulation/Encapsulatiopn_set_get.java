package ALL_Encapsulation;

public class Encapsulatiopn_set_get {
    private String name;
    private int id, age;
    
    public void set_name(String name){
        this.name = name;
    }
    public String get_name(){
        return name;
    }
    
    public void set_ID(int id){
         this.id = id;
    }
    public int get_ID(){
         return id;
    }
    
    public void set_age(int age){
         this.age = age;
    }
    public int get_age(){
         return age;
    }
}
